<p style="margin-left:20px;margin-right:10px;">Pusat Pendayagunaan Informatika dan Kawasan Strategis Nuklir (PPIKSN)
merupakan salah satu unit kerja di BATAN yang mempunyai tusi untuk mendukung 
pelaksanaan kegiatan penelitian, khususnya dalam bidang teknologi informasi dan 
pengelolaan Kawasan Nuklir Serpong (KNS). Untuk mendukung pengelolaan layanan di 
PPIKSN yang lebih efektif, efisien dan terpadu, maka perlu dilakukan pengembangan 
sistem informasi berbasis web yang diberi nama Sistem Aplikasi FAQ (Frequently Asked Questions) 
Teknologi Nuklir.
<p style="margin-left:20px;margin-right:30px;">Dengan dibangunnya sistem informasi ini, diharapkan data dan informasi yang 
dihasilkan dapat digunakan oleh para pemangku kepentingan. Secara teknis hal ini dapat dilakukan 
dengan adanya teknologi web yang digunakan dan sumberdaya serta pengalaman yang memadai selama 
pengembangan sistem informasi. Kegiatan pengembangan ini akan dilakukan dengan menggunakan 
Open Source Software (OSS) yang berbasis web, sehingga biaya pengembangan sistem informasi ini 
dapat ditekan serendah mungkin.</p>